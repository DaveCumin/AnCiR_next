def _py_repr(v):
    """Repr that JSON-friendly-ifies floats and None."""
    if isinstance(v, float):
        if v != v:  # NaN
            return "float('nan')"
        if v == float("inf"):
            return "float('inf')"
        if v == float("-inf"):
            return "float('-inf')"
    return repr(v)


def _col_meta_for_column(col):
    """Pluck the JSON-serialisable metadata for a column object."""
    meta = {
        "name": col.get("name"),
        "type": col.get("type", "number"),
        "data": col.get("data"),
        "compression": col.get("compression"),
        "timeFormat": col.get("timeFormat"),
        "binWidth": col.get("binWidth", 1.0),
        "refId": col.get("refId"),
        "processes": [
            {
                "funcname": p.get("funcname"),
                "name": p.get("name"),
                "args": p.get("args", {}),
            }
            for p in col.get("processes", [])
        ],
    }
    return {k: v for k, v in meta.items() if v is not None or k in ("processes",)}


def emit(session_path: Path, output_path: Path) -> None:
    with open(session_path) as f:
        session = json.load(f)

    runtime_src = RUNTIME_PATH.read_text()
    # Strip the runtime's own imports — we add them in HEADER — and dedent
    runtime_lines = []
    for line in runtime_src.splitlines():
        if line.startswith("from __future__") or line.startswith("import ") \
                or line.startswith("from "):
            # keep typing imports; drop numpy/pandas/scipy since HEADER has them
            if "numpy" in line or "pandas" in line or "scipy" in line \
                    or line.startswith("from __future__"):
                continue
        runtime_lines.append(line)
    runtime_block = "\n".join(runtime_lines)

    raw_data = session.get("rawData", {})
    columns = session.get("data", [])
    # Analyses are free table-process nodes under `tableProcesses` (the `tables`
    # grouping was removed 2026-06-15). Legacy sessions that still carry grouped
    # `tables` are flattened in for back-compat.
    table_processes = session.get("tableProcesses", [])
    legacy_tables = session.get("tables", [])
    plots = session.get("plots", [])
    stored_values = session.get("storedValues", {}) or {}

    # Build column metadata dict
    col_meta = {}
    for c in columns:
        cid = c.get("id")
        if cid is None:
            continue
        col_meta[cid] = _col_meta_for_column(c)

    # Each table process keeps its registry name (AnCiR uses `name`, e.g.
    # 'Cosinor', 'GroupComparison'); the runtime maps name → tp-key. Flatten the
    # free `tableProcesses` plus any legacy grouped `tables` into one ordered list.
    tps_clean = [
        {"name": tp.get("name"), "args": tp.get("args", {})}
        for tp in table_processes
    ]
    for t in legacy_tables:
        for p in t.get("processes", []):
            tps_clean.append({"name": p.get("name"), "args": p.get("args", {})})

    # Embed as JSON strings parsed at startup so null/true/false stay valid.
    # Convert string keys back to int for COLUMN_META so column ids match.
    parts = [HEADER, "", runtime_block, ""]
    parts.append("# ----- Session data (embedded) -----")
    parts.append(f"RAW_DATA = json.loads({json.dumps(json.dumps(raw_data))})")
    parts.append("RAW_DATA = {int(k) if k.lstrip('-').isdigit() else k: v "
                 "for k, v in RAW_DATA.items()}")
    parts.append(f"_COL_META_RAW = json.loads({json.dumps(json.dumps(col_meta))})")
    parts.append("COLUMN_META = {int(k): v for k, v in _COL_META_RAW.items()}")
    parts.append(f"TABLE_PROCESSES = json.loads({json.dumps(json.dumps(tps_clean))})")
    parts.append(f"PLOTS = json.loads({json.dumps(json.dumps(plots))})")
    parts.append(f"STORED_VALUES = json.loads({json.dumps(json.dumps(stored_values))})")
    parts.append(FOOTER_RUN)

    output_path.write_text("\n".join(parts))
    print(f"[ancir-emit] wrote {output_path} "
          f"({len(columns)} columns, {len(tps_clean)} table processes)")


def main(argv=None):
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("session", type=Path, help="AnCiR session.json")
    p.add_argument("-o", "--output", type=Path, default=Path("analysis.py"),
                   help="output Python script (default: analysis.py)")
    args = p.parse_args(argv)
    if not args.session.exists():
        print(f"error: {args.session} not found", file=sys.stderr)
        return 2
    emit(args.session, args.output)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
