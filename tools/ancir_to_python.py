    for k in rows:
        rows[k] = list(rows[k]) + [None] * (max_len - len(rows[k]))
    df_cols = pd.DataFrame(rows)
    df_cols.to_csv(out_path / "columns.csv", index=False)
    print(f"[ancir] wrote {out_path / 'columns.csv'} ({len(df_cols)} rows, {len(df_cols.columns)} cols)")

    # Run table processes: each writes into RAW_DATA[out_id], so column data is
    # updated in-place. After running, dump every column to CSV. Free table-
    # process nodes are stored in creation order (oldest first), which respects
    # dependencies, so they run in array order.
    #
    # By default the runtime is strict (STRICT_PROCESSES = True): an unknown
    # process raises UnsupportedProcessError and aborts here rather than
    # silently producing wrong output. Set STRICT_PROCESSES = False above to
    # continue past unknown processes (output WILL be wrong); they are then
    # listed in the summary below.
    for tp in TABLE_PROCESSES:
        ok = run_table_process(tp["name"], tp["args"],
                               columns_index, RAW_DATA, stored)
        print(f"[ancir]   {tp['name']}: {'ok' if ok else 'skipped/empty'}")

