/**
 * Strip the runtime's own numpy/pandas/scipy/`from __future__` imports — HEADER
 * re-declares them. Mirrors the loop in ancir_to_python.py:emit().
 * @param {string} runtimeSrc
 * @returns {string}
 */
function stripRuntimeImports(runtimeSrc) {
	const kept = [];
	for (const line of runtimeSrc.split('\n')) {
		if (
			line.startsWith('from __future__') ||
			line.startsWith('import ') ||
			line.startsWith('from ')
		) {
			if (
				line.includes('numpy') ||
				line.includes('pandas') ||
				line.includes('scipy') ||
				line.startsWith('from __future__')
			) {
				continue;
			}
		}
		kept.push(line);
	}
	return kept.join('\n');
}

/**
 * Pluck the JSON-serialisable metadata for a column. Mirrors
 * ancir_to_python.py:_col_meta_for_column — keys whose value is null are
 * dropped (except `processes`, which is always kept).
 * @param {any} col
 */
function colMetaForColumn(col) {
	const meta = {
		name: col.name ?? null,
		type: col.type ?? 'number',
		data: col.data ?? null,
		compression: col.compression ?? null,
		timeFormat: col.timeFormat ?? null,
		binWidth: col.binWidth ?? 1.0,
		refId: col.refId ?? null,
		groupLabel: col.groupLabel ?? null,
		processes: (col.processes ?? []).map((p) => ({
			funcname: p.funcname ?? null,
			name: p.name ?? null,
			args: p.args ?? {}
		}))
	};
	const out = {};
	for (const [k, v] of Object.entries(meta)) {
		if (v !== null || k === 'processes') out[k] = v;
	}
	return out;
}

// Embed a value the way the CLI does: `json.loads("<escaped json text>")`.
// JSON.stringify of the JSON text yields a string literal valid in both JSON
// and Python, mirroring the CLI's json.dumps(json.dumps(x)).
function embed(name, value) {
	return `${name} = json.loads(${JSON.stringify(JSON.stringify(value))})`;
}

/**
 * Build a self-contained Python script reproducing `session`.
 * @param {any} session - parsed session object (as from outputCoreAsJson()).
 * @param {string} runtimeSrc - verbatim source of tools/ancir_runtime.py.
 * @returns {string} the Python script text.
 */
export function sessionToPython(session, runtimeSrc) {
	if (!session || typeof session !== 'object') {
		throw new Error('sessionToPython: invalid session object');
	}
	if (typeof runtimeSrc !== 'string' || !runtimeSrc) {
		throw new Error('sessionToPython: missing runtime source');
	}

	const runtimeBlock = stripRuntimeImports(runtimeSrc);

	const rawData = session.rawData ?? {};
	const columns = session.data ?? [];
	// Analyses are free table-process nodes; legacy grouped `tables` are flattened
	// in for back-compat (mirrors the CLI).
	const tableProcesses = session.tableProcesses ?? [];
	const legacyTables = session.tables ?? [];
	const plots = session.plots ?? [];
	const storedValues = session.storedValues ?? {};

	/** @type {Record<number, any>} */
	const colMeta = {};
	for (const c of columns) {
		if (c?.id == null) continue;
		colMeta[c.id] = colMetaForColumn(c);
	}

	const tpsClean = [];
	for (const tp of tableProcesses) {
		tpsClean.push({ name: tp.name ?? null, args: tp.args ?? {} });
	}
	for (const t of legacyTables) {
		for (const p of t.processes ?? []) {
			tpsClean.push({ name: p.name ?? null, args: p.args ?? {} });
		}
	}

	const parts = [HEADER, '', runtimeBlock, ''];
	parts.push('# ----- Session data (embedded) -----');
	parts.push(embed('RAW_DATA', rawData));
	parts.push(
		"RAW_DATA = {int(k) if k.lstrip('-').isdigit() else k: v for k, v in RAW_DATA.items()}"
	);
	parts.push(embed('_COL_META_RAW', colMeta));
	parts.push('COLUMN_META = {int(k): v for k, v in _COL_META_RAW.items()}');
	parts.push(embed('TABLE_PROCESSES', tpsClean));
	parts.push(embed('PLOTS', plots));
	parts.push(embed('STORED_VALUES', storedValues));
	parts.push(FOOTER_RUN);

	return parts.join('\n');
}
